<a name="compra"></a><h2>Compra de libros.</h2>
<ul>
<li><strong>Pago y envío</strong> - Sólo se aceptan tarjetas de crédito como método de pago. La mercadería será enviada al lugar de residencia detallado en su perfil. </li>
<li><strong>Carrito de compras</strong> - Aquí se muestra la lista de libros que ha agregado a su carrito de compras. Puede observar la cantidad, el precio unitario y precio total por cada libro.</li>
	Si desea remover un ejemplar de la lista, simplemente haga click en "Quitar". Si desea vaciar su carrito completamente, haga click en "Vaciar carrito".</br>
	Una vez que se encuentre satisfecho con su pedido, proceda a hacer efectiva la compra haciendo click en "Realizar compra".
<li><strong>Tarjeta de crédito</strong> - Se requieren los siguientes datos para efectuar su compra: <i><strong>titular</strong></i> de la tarjeta, <i><strong>número</strong></i> (16 dígitos), <i><strong>código de verificación</strong></i> (3 dígitos) y la <i><strong>fecha de vencimiento</strong></i>.</li>
	Una vez verificados los datos de su tarjeta, tendrá la opción de confirmar la compra. Una vez hecha efectiva, podrá ver su estado en "Pedidos".
<a href="#indice" title="Ir al índice">↑</a>
</ul>

<hr>

<a name="pedidos"></a><h2>Pedidos de compra.</h2>
<ul>
<li><strong>Lista de pedidos</strong> - En esta sección, usted podrá observar los detalles y el estado de sus pedidos vigentes.</li>
<li><strong>Información detallada</strong> - En "Ver Detalle" tendrá acceso a los datos del pedido. La fecha en la que se realizó, el monto, estado y la lista de libros solicitados.</li>
	"Ver Comprobante" generará un comprobante de compra, en el que se precisan los datos del envío. Usted dispone de la opción para imprimirlo.
<li><strong>Estado actual</strong> - Un pedido tiene tres estados posibles.</li>
	<ul>
	<li><i><strong>Pendiente</strong></i>: El pedido no ha sido enviado todavía. Se realizan envíos todos los días hábiles, por lo que no permanecerá mucho tiempo en este estado.</li>
	<li><i><strong>Enviado</strong></i>: El pedido se encuentra en camino. Una vez que reciba la mercadería solicitada, haga click en "Cambiar estado" para confirmar su arribo.</li>
	<li><i><strong>Finalizado</strong></i>: El pedido ha sido resuelto. Desaparecerá de la lista.</li>
	</ul>
<li><strong>¿Preguntas?</strong> - Ante cualquier consulta o inconveniente que surga, pónganse en contacto con los administradores del sistema. <a href="#indice" title="Ir al índice">↑</a></li>
</ul>

