<a name="cuenta"></a><h2>Cuenta de usuario.</h2>
<ul>
<li><strong>Registro de cuenta</strong> - Para registrarse en el sistema de ventas Cookbook, se requieren sus datos personales.</li>
	La <i><strong>cuenta de correo</strong></i> es utilizada como identificador para ingresar al sistema, junto con la <i><strong>contraseña</strong></i>.</br>
	Su <i><strong>nombre completo</strong></i> y <i><strong>lugar de residencia</strong></i> (provincia, localidad y domicilio) son utilizados para el envío de mercadería.</br>
	Su <i><strong>número de DNI</strong></i> será usado para verificar su identidad al momento de recibir su pedido.</br>
	Sólo lo llamaremos al <i><strong>número de teléfono</strong></i> si existe algún problema con su pedido y no podemos contactarlo vía email.</br>
	Se urge la entrada de datos verdaderos, actualizados y no ofensivos para asegurar un mejor servicio al momento de la compra. Muchas gracias.
<li><strong>Inicio de sesión</strong> - Ingrese la dirección de correo y contraseña que utilizó al momento de registrarse para acceder a las funcionalidades avanzadas del sistema.</li>
	Si surgen problemas al momento de acceder, es posible que su cuenta haya sido bloqueada (por ingreso de datos o envío de mensajes ofensivos), dada de baja (función voluntaria del usuario) o se haya olvidado su contraseña. En este último caso, deberá seleccionar la opción "Restablecer la contraseña".
<li><strong>Recuperar contraseña</strong> -
	Ingrese la dirección de correo con la cual se registró. Un email será enviado a su casilla con las instrucciones a seguir.</li>
<li><strong>Modificar perfil</strong> - En todo momento, podrá modificar y actualizar sus datos personales.</li>
	Usted tendrá la opción de darse de baja, si es que desea dejar de utilizar nuestro sistema. Esta operación NO puede deshacerse.</br>
	Será informado si posee pedidos sin finalizar. Por favor, aguarde el arribo de los mismos, para asegurarse de que todo se encuentre en orden. Muchas gracias.
<a href="#indice" title="Ir al índice">↑</a><P>
</ul>

