<a name="catalogo"></a><h2>Catálogo de libros.</h2> 
<ul>
<li><strong>Inventario de libros</strong> - En la página de inicio, verá a simple vista el inventario de libros que Cookbook dispone para ofrecerle.</li>
	Usted podrá buscar un libro específico por su <i><strong>título</strong></i>, <i><strong>ISBN</strong></i> o <i><strong>autor</strong></i>. Adicionalmente, podrá filtrar por <i><strong>editorial</strong></i> o <i><strong>etiqueta</strong></i>.</br>
	Si un libro no se encuentra disponible para la compra, se reflejará como agotado en el catálogo.
<li><strong>Detalles de libro</strong> - Para acceder a los detalles, primero deberá iniciar sesión en el sistema.</li>
	En esta sección, podrá examinar detenidamente los datos del libro seleccionado. Además, se ofrece un escaneo del índice, para que pueda determinar su contenido.</br>
	Si está interesado en la adquisición de un ejemplar, haga click en "Agregar al carrito". Cada vez que presione ese botón, se agregará una copia.</br>
	Podrá deshacer o proceder con la compra desde su Carrito.
<a href="#indice" title="Ir al índice">↑</a>
</ul>

